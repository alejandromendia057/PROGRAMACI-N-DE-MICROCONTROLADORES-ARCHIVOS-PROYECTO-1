// Encabezado (Definición de Registros, Variables y Constantes)
.include "M328PDEF.inc"     // Include definitions specific to ATMega328P
.dseg
.org    SRAM_START
// VARIABLES EN SRAM (MEMORIA DE DATOS)
OVERFLOW_TMR0:			.byte 1		// CONTADOR DE DESBORDAMIENTOS DEL TIMER0 PARA LOS LEDS DE EN MEDIO (250MS)					
INDICADOR_TMR1:			.byte 1		// BANDERA QUE INDICA QUE PASO 1 MINUTO
C_UNIDAD_MINUTO:		.byte 1		// CONTADOR UNIDADES DE MINUTO (0-9)
C_DECENA_MINUTO:		.byte 1		// CONTADOR DECENAS DE MINUTO (0-5)
C_UNIDAD_HORA:			.byte 1		// CONTADOR UNIDADES DE HORA (0-9)
C_DECENA_HORA:			.byte 1		// CONTADOR DECENAS DE HORA (0-2)
DISP_U_MIN:				.byte 1		// CÓDIGO DE 7 SEGMENTOS PARA UNIDAD DE MINUTO
DISP_D_MIN:				.byte 1		// CÓDIGO DE 7 SEGMENTOS PARA DECENA DE MINUTO
DISP_U_HR:				.byte 1		// CÓDIGO DE 7 SEGMENTOS PARA UNIDAD DE HORA
DISP_D_HR:				.byte 1		// CÓDIGO DE 7 SEGMENTOS PARA DECENA DE HORA
DISP_SELEC:				.byte 1		// SELECTOR DE DÍGITO PARA MULTIPLEXADO (0-3)
MODO_ESTADO:			.byte 1		// ESTADO ACTUAL DE LA MÁQUINA (00 MOSTRAR HORA, 01 MOSTRAR FECHA, 10 CONFIGURAR HORA Y SET ALARMA, 11 CONFIGURAR FECHA)
PINC_ANTERIOR:			.byte 1		// ÚLTIMO VALOR LEÍDO DE PINC (PARA DETECCIÓN DE FLANCOS)	
INDICADOR_BOTON1:		.byte 1		// BANDERA PARA INDICAR SI BOTÓN 1 FUE PRESIONADO 
INDICADOR_BOTON2:		.byte 1		// BANDERA PARA INDICAR SI BOTÓN 2 FUE PRESIONADO 
INDICADOR_BOTON3:		.byte 1		// BANDERA PARA INDICAR SI BOTÓN 3 FUE PRESIONADO 
INDICADOR_BOTON4:		.byte 1		// BANDERA PARA INDICAR SI BOTÓN 4 FUE PRESIONADO 
INDICADOR_BOTON5:		.byte 1		// BANDERA PARA INDICAR SI BOTÓN 5 FUE PRESIONADO 
C_UNIDAD_MES:			.byte 1		// CONTADOR UNIDADES DE MES (0-9)
C_DECENA_MES:			.byte 1		// CONTADOR DECENAS DE MES (0-1)
C_UNIDAD_DIA:			.byte 1		// CONTADOR UNIDADES DE DÍA (0-9)
C_DECENA_DIA:			.byte 1		// CONTADOR DECENAS DE DÍA (0-3)
DISP_U_MES:				.byte 1		// CÓDIGO DE 7 SEGMENTOS PARA UNIDAD DE MES 
DISP_D_MES:				.byte 1		// CÓDIGO DE 7 SEGMENTOS PARA DECENA DE MES 
DISP_U_DIA:				.byte 1		// CÓDIGO DE 7 SEGMENTOS PARA UNIDAD DE DÍA
DISP_D_DIA:				.byte 1		// CÓDIGO DE 7 SEGMENTOS PARA DECENA DE DÍA
MODO_DISPLAY:			.byte 1		// MODO DE VISUALIZACIÓN (0 HORA, 1 FECHA, 2 CONF DE HORA, 3 CONF DE FECHA)
INDICADOR_CAMBIO_DIA:	.byte 1		// BANDERA QUE INDICA EL PASO DE UN DÍA 
DISP_SELEC_ESTADO3:		.byte 1		// SELECTOR DE DÍGITO EN MODO CONFIGURACIÓN DE FECHA
DISP_ESTADO3_1:			.byte 1		// VARIABLE TEMPORAL PARA CONTROLAR DISPLAY DEL DÍGITO 1 EN EL ESTADO 3
DISP_ESTADO3_2:			.byte 1		// VARIABLE TEMPORAL PARA CONTROLAR DISPLAY DEL DÍGITO 2 EN EL ESTADO 3
DISP_ESTADO3_3:			.byte 1		// VARIABLE TEMPORAL PARA CONTROLAR DISPLAY DEL DÍGITO 3 EN EL ESTADO 3
DISP_ESTADO3_4:			.byte 1		// VARIABLE TEMPORAL PARA CONTROLAR DISPLAY DEL DÍGITO 4 EN EL ESTADO 3
U_MES_ESTADO3:			.byte 1		// VALOR TEMPORAL UNIDAD DE MES EN CONFIGURACIÓN DE FECHA 
D_MES_ESTADO3:			.byte 1		// VALOR TEMPORAL DECENA DE MES EN CONFIGURACIÓN DE FECHA
U_DIA_ESTADO3:			.byte 1		// VALOR TEMPORAL UNIDAD DE DIA EN CONFIGURACIÓN DE FECHA
D_DIA_ESTADO3:			.byte 1		// VALOR TEMPORAL DECENA DE DIA EN CONFIGURACIÓN DE FECHA
DISP_SELEC_ESTADO2:		.byte 1		// SELECTOR DE DÍGITO EN MODO CONFIGURACIÓN HORA (0-3)
DISP_ESTADO2_1:			.byte 1		// VARIABLE TEMPORAL PARA CONTROLAR DISPLAY DEL DÍGITO 1 EN EL ESTADO 2
DISP_ESTADO2_2:			.byte 1		// VARIABLE TEMPORAL PARA CONTROLAR DISPLAY DEL DÍGITO 2 EN EL ESTADO 2
DISP_ESTADO2_3:			.byte 1		// VARIABLE TEMPORAL PARA CONTROLAR DISPLAY DEL DÍGITO 3 EN EL ESTADO 2
DISP_ESTADO2_4:			.byte 1		// VARIABLE TEMPORAL PARA CONTROLAR DISPLAY DEL DÍGITO 4 EN EL ESTADO 2
U_MIN_ESTADO2:			.byte 1		// VALOR TEMPORAL UNIDAD DE MINUTO EN CONFIGURACIÓN DE HORA
D_MIN_ESTADO2:			.byte 1		// VALOR TEMPORAL DECENA DE MINUTO EN CONFIGURACIÓN DE HORA
U_HR_ESTADO2:			.byte 1		// VALOR TEMPORAL UNIDAD DE HORA EN CONFIGURACIÓN DE HORA
D_HR_ESTADO2:			.byte 1		// VALOR TEMPORAL DECENA DE HORA EN CONFIGURACIÓN DE HORA
U_MIN_ALARMA:			.byte 1		// UNIDAD DE MINUTO DE LA ALARMA 
D_MIN_ALARMA:			.byte 1		// DECENA DE MINUTO DE LA ALARMA
U_HR_ALARMA:			.byte 1		// UNIDAD DE HORA DE LA ALARMA 
D_HR_ALARMA:			.byte 1		// DECENA DE HORA DE LA ALARMA 
CONTADOR_ALARMA:		.byte 1		// CONTADOR PARA TIEMPO DE SONIDO DE ALARMA (DENTRO DE ISR0)
BUZZER_ENCENDIDO:		.byte 1		// BANDERA QUE INDICA SI EL BUZER ESTÁ SONANDO 

.cseg
.org 0x0000
	JMP START						// SALTO INICIO DE PROGRAMA 						
.org PCI1addr						// NOMBRE RESERVADO PARA LA DIRECCIÓN 0x0008 ; HABILITA INTERRUPCIONES PINCHANGE EN PORTC (BOTONES)  
	JMP PINC_ISR				
.org OVF2addr						// NOMBRE RESERVADO PARA LA DIRECCIÓN 0x0016 ; HABILITA INTERRUPCIONES PARA TIMER2 (MULTIPLEXADO)
	JMP ISR_TIMER2
.org OVF1addr						// NOMBRE RESERVADO PARA LA DIRECCIÓN 0x001A ; HABILITA INTERRUPCIONES PARA TIMER 1 (MINUTOS)		
	JMP ISR_TIMER1			
.org OVF0addr						// NOMBRE RESERVADO PARA LA DIRECCIÓN 0x0020 ; HABILITA INTERRUPCIONES PARA TIMER0 (LEDS DE EN MEDIO Y ALARMA)
	JMP ISR_TIMER0
 /******/
// Configuración de la pila

START:
LDI     R16, LOW(RAMEND)
OUT     SPL, R16
LDI     R16, HIGH(RAMEND)
OUT     SPH, R16

// TABLA DE CONVERSIÓN A 7 SEGMENTOS PARA (CÁTODO COMÚN)

tabla7: .DB 0x7D, 0x48, 0x3E, 0x6E, 0x4B, 0x67, 0x73, 0x4E, 0x7F, 0x4F

/******/
// CONFIGURACIÓN DEL MICROCONTROLADOR 

SETUP:
	CLI								// SE DESHABILITA INTERRUPCIONES MIENTRAS SE CONFIGURA 
	
	// SETTING PRESCALER A 1MHZ 
	LDI R16, (1<<CLKPCE)
	STS CLKPR, R16						
	LDI R16, 0b0000_0100
	STS CLKPR, R16

	// CONFIGURACIÓN DEL TIMER0 CON DELAY DE 250 MS PARA LOS LEDS DE EN MEDIO Y CONTROL DE LA ALARMA  
	LDI R16, 0x0
	OUT TCCR0A, R16						// RELOJ MODO NORMAL 
	LDI R16, 0b0000_0101				
	OUT TCCR0B, R16						// PRESCALER 1024
	LDI R16, 0x0C						
	OUT TCNT0, R16						// VALOR INICIAL DE 12 

	// CONFIGURACIÓN DE INTERRUPCIÓN PARA TIMER0 
	LDI R16, (1<< TOIE0)				 
	STS TIMSK0, R16				 

	// CONFIGURACIÓN DEL TIMER2 CON DELAY DE 2 MS PARA MULTIPLEXADO
	LDI R16, 0x0 
	STS TCCR2A, R16						// RELOJ MODO NORMAL 
	LDI R16, 0b0000_0010
	STS TCCR2B, R16						// PRESCALER DE 8
	LDI R16, 0x06 
	STS TCNT2, R16						// VALOR INICIAL DE 6 

	// CONFIGURACIÓN DE INTERRUPCIÓN PARA TIMER2 
	LDI R16, (1<< TOIE2)
	STS TIMSK2, R16						

	// CONFIGURACIÓN DEL TIMER1 CON DELAY DE 60 SEGUNDOS PARA RELOJ 
	LDI R16, 0x0 
	STS TCCR1A, R16						// RELOJ MODO NORMAL				
	LDI R16, 0b0000_0101
	STS TCCR1B, R16						// PRESCALER DE 1024 
	LDI R16, 0x1B							
	STS TCNT1H, R16						// CARGA AL BYTE ALTO: 1B
	LDI R16, 0x1F		  
	STS TCNT1L, R16						// CARGA AL BYTE BAJO: 1F PARA HACER UN TOTAL DE 6943 

	// CONFIGURACIÓN DE INTERRUPCIÓN PARA TIMER 1 
	LDI R16, (1<< TOIE1)
	STS TIMSK1, R16					

	// CONFIGURACIÓNDE INTERRUPCIÓN POR CAMBIO DE PIN EN PORTC (BOTONES)
	LDI R16, (1 << PCIE1)
	STS PCICR, R16 
	LDI R16, (1 << PCINT12)| (1 << PCINT11)| (1 << PCINT10)| (1 << PCINT9)| (1 << PCINT8)
	STS PCMSK1, R16 
	
	// CARGA DE PRIMER VALOR DE LA TABLA EN PORTD 
	LDI ZH, HIGH(tabla7<<1)
	LDI ZL, LOW(tabla7<<1)
	LPM R17, Z				 
	OUT PORTD, R17

	// CONFIGURACIÓN DE ENTRADAS Y SALIDAS 

	// CONFIGURACIÓN DE SALIDAS PARA LOS SEGMENTOS DE LOS DISPLAYS DEL PUERTO D Y EL BUZZER
	LDI R16, 0b1111_1111
	OUT DDRD, R16 
	LDI R16, 0b0000_0000 
	OUT PORTD, R16

	//CONFIGURACIÓN DE SALIDAS PARA TRANSISTORES DE CONTROL DEL DISPLAY 
	SBI DDRB, DDB0						// SALIDA TRANSISTOR DÍGITO 1 
	CBI PORTB, PB0							
	SBI DDRB, DDB1						// SALIDA TRANSISTOR DÍGITO 2 
	CBI PORTB, PB1 
	SBI DDRB, DDB2						// SALIDA TRANSISTOR DÍGITO 3 
	CBI PORTB, PB2
	SBI DDRB, DDB3						// SALIDA TRANSISTOR DÍGITO 4 
	CBI PORTB, PB3  

	// CONFIGURACIÓN DE SALIDA PARA LOS LEDS DE EN MEDIO 
	SBI DDRC, DDC5						// SALIDA PARA LOS LEDS EN PARALELO QUE ESTÁN EN MEDIO DE LOS 4 DISPLAYS
	CBI PORTC, PC5

	// CONFIGURACIÓN DE ENTRADA CON PULL-UP PARA LOS BOTONES DEL PORTC 
	CBI DDRC, DDC0 
	SBI PORTC, PORTC0
	CBI DDRC, DDC1 
	SBI PORTC, PORTC1
	CBI DDRC, DDC2 
	SBI PORTC, PORTC2
	CBI DDRC, DDC3 
	SBI PORTC, PORTC3
	CBI DDRC, DDC4 
	SBI PORTC, PORTC4

	// CONFIGURACIÓN DE SALIDA PARA LOS LEDS QUE MUESTRAN MODO 
	SBI DDRB, DDB4 
	CBI PORTB, PB4 
	SBI DDRB, DDB5 
	CBI PORTB, PB5

	// DESACTIVAR UART (PARA QUE NO HAYAN PROBLEMAS CON TX y RX)
	LDI R16, 0x00
	STS UCSR0B, R16

	// INICIALIZACIÓN DE VARIABLES 

	CLR R1									// R1 SIEMPRE ES CERO, PARA SUMAS CON ACARREO 
	CLR R16									
	STS OVERFLOW_TMR0, R16					
	STS INDICADOR_TMR1, R16					
	STS C_UNIDAD_MINUTO, R16				 
	STS C_DECENA_MINUTO, R16				 
	STS C_UNIDAD_HORA, R16					
	STS C_DECENA_HORA, R16					
	STS DISP_U_MIN, R16						
	STS DISP_D_MIN, R16						
	STS DISP_U_HR, R16						
	STS DISP_D_HR, R16						 
	STS DISP_SELEC, R16						
	STS MODO_ESTADO, R16					
	STS PINC_ANTERIOR, R16					 
	STS INDICADOR_BOTON1, R16				 	
	STS INDICADOR_BOTON2, R16				
	STS INDICADOR_BOTON3, R16				
	STS INDICADOR_BOTON4, R16				
	STS INDICADOR_BOTON5, R16				
	STS C_DECENA_MES, R16					
	STS C_DECENA_DIA, R16					
	STS DISP_U_MES, R16						
	STS DISP_D_MES, R16						
	STS DISP_U_DIA, R16						
	STS DISP_D_DIA, R16						
	STS MODO_DISPLAY, R16					
	STS INDICADOR_CAMBIO_DIA, R16			
	STS DISP_ESTADO3_1, R16					
	STS DISP_ESTADO3_2, R16					
	STS DISP_ESTADO3_3, R16					
	STS DISP_ESTADO3_4, R16					
	STS U_MES_ESTADO3, R16					
	STS D_MES_ESTADO3, R16 					
	STS U_DIA_ESTADO3, R16 					
	STS D_DIA_ESTADO3, R16 					
	STS DISP_ESTADO2_1, R16					
	STS DISP_ESTADO2_2, R16					
	STS DISP_ESTADO2_3, R16					
	STS DISP_ESTADO2_4, R16					
	STS U_MIN_ESTADO2, R16					
	STS D_MIN_ESTADO2, R16 					
	STS U_HR_ESTADO2, R16 					
	STS D_HR_ESTADO2, R16 					
	STS CONTADOR_ALARMA, R16				
	STS BUZZER_ENCENDIDO, R16
	// VALORES INICIALES PARA FECHA (1 DE ENERO)				
	LDI R16, 0x01 
	STS C_UNIDAD_MES, R16					
	STS C_UNIDAD_DIA, R16					
	LDI R16, 0x03 
	STS DISP_SELEC_ESTADO3, R16				
	STS DISP_SELEC_ESTADO2, R16
	// ALARMA_ VALORES INICIALES 0xFF PARA QUE NO COINCIDA CON NINGUNA HORA REAL 				
	LDI R16, 0xFF							
	STS U_MIN_ALARMA, R16 					
	STS D_MIN_ALARMA, R16 					
	STS U_HR_ALARMA, R16 					
	STS	D_HR_ALARMA, R16 					
	SEI										
/******/
// PROGRAMA PRINCIPAL (LOOP INFINITO)
MAIN_LOOP:									
	CALL RELOJ								// ACTUALIZA LA HORA SI HA PASADO UN MINUTO					
	CALL FECHA								// ACTUALIZA LA FECHA SI HA PASADO UN DÍA 
	CALL BOTON1								// PROCESO BOTÓN 1 SEGÚN ESTADO 
	CALL BOTON2								// PROCESO BOTÓN 2 SEGÚN ESTADO
	CALL BOTON3								// PROCESO BOTÓN 3 SEGÚN ESTADO
	CALL BOTON4								// PROCESO BOTÓN 4 SEGÚN ESTADO
	CALL BOTON5								// PROCESO BOTÓN 5 (CAMBIO DE ESTADO)
	CALL ALARMA								// COMPRUEBA SI ES HORA DE LA ALARMA 
	RJMP MAIN_LOOP
  
RELOJ: 
	LDS R16, INDICADOR_TMR1 
	CPI R16, 0x01							// COMPARA EL INDICADOR DE TIMER 1 CON 1 PARA SABER SI YA PASÓ UN MINUTO
	BRNE ACTUALIZACION_DISPLAYS 
	CLR R16									 
	STS INDICADOR_TMR1, R16					// SE LIMPIA EL INDICADOR DE TIMER 1
	LDS R16, C_UNIDAD_MINUTO				// INCREMENTA EL CONTADOR DE UNIDADES DE MINUTOS 
	INC R16									
	STS C_UNIDAD_MINUTO, R16
	CPI R16, 0x0A							// COMPARO EL CONTADOR DE UNIDADES DE MINUTO CON 10 PARA SABER SI YA HUBO OVERFLOW 
	BRNE ACTUALIZACION_DISPLAYS			
	CLR R16
	STS C_UNIDAD_MINUTO, R16 
	LDS R16, C_DECENA_MINUTO				// INCREMENTA DECENAS DE MINUTO 
	INC R16									
	STS C_DECENA_MINUTO, R16
	CPI R16, 0x6							// COMPARO EL CONTADOR DE DECENAS DE MINUTO CON 6 PARA SABER SI YA PASO UN MINUTO
	BRNE ACTUALIZACION_DISPLAYS 
	CLR R16									 
	STS C_DECENA_MINUTO, R16				// CUANDO LO MINUTOS PASEN DE 59, DEBE REINICIARSE EL CONTADOR DE DECENAS Y UNIDADES DE MINUTO
	STS C_UNIDAD_MINUTO, R16
	LDS R16, C_UNIDAD_HORA					// INCREMENTA EL CONTADOR DE UNIDADES DE HORA
	INC R16									 
	STS C_UNIDAD_HORA, R16 

	CPI R16, 0x4							// COMPRUEBA SI SON LAS 24:00 (UNIDAD=4 Y DECENA=2)
	BRNE LOGICA_NORMAL_HORA
	LDS R16, C_DECENA_HORA
	CPI R16, 0x2
	BRNE LOGICA_NORMAL_HORA
	
	CLR R16									// REINICIO A 00:00 Y ACTIVA CAMBIO DE DÍA 
	STS C_UNIDAD_MINUTO, R16 
	STS C_DECENA_MINUTO, R16 
	STS C_UNIDAD_HORA, R16
	STS C_DECENA_HORA, R16
	LDS R16, INDICADOR_CAMBIO_DIA			// SI PASO UN DÍA, SE ENCIENDE LA BANDERA INDICADOR_CAMBIO_DIA LA CUAL ES ÚTIL PARA LA RUTINA FECHA
	LDI R16, 0x01 
	STS INDICADOR_CAMBIO_DIA, R16 
	RJMP ACTUALIZACION_DISPLAYS 

LOGICA_NORMAL_HORA: 
	LDS R16, C_UNIDAD_HORA
	CPI R16, 0xA							// COMPARO EL CONTADOR DE UNIDADES DE HORA CON 10, SIGNIFICA QUE PUEDO SUMAR LAS DECENAS DE HORA 					
	BRNE ACTUALIZACION_DISPLAYS 
	CLR R16
	STS C_UNIDAD_HORA, R16
	LDS R16, C_DECENA_HORA
	INC R16									// INCREMENTA EL CONTADOR DE DECENAS DE HORA
	STS C_DECENA_HORA, R16		

ACTUALIZACION_DISPLAYS:						
	CALL MOSTRAR_CONTADOR_TIEMPO			// SE ACTUALIZAN LOS DISPLAYS PARA LA HORA 
	RET 

FECHA:										// RUTINA DE ACTUALIZACIÓN DE LA FECHA 								
	LDS R16, INDICADOR_CAMBIO_DIA			// SE EJECUTA CUANDO INDICADOR_CAMBIO_DIA= 1 (CADA 24 HORAS)
	CPI R16, 0x01 
	BREQ INCREMENTAR_FECHA
	RJMP ACTUALIZACION_DISPLAYS_FECHA
INCREMENTAR_FECHA: 
	CLR R16
	STS INDICADOR_CAMBIO_DIA, R16 
	LDS R16, C_UNIDAD_DIA					// SI PASO UN DÍA INCREMENTA EL CONTADOR DE UNIDAD DE DÍA
	INC R16 
	STS C_UNIDAD_DIA, R16  
	CPI R16, 0xA							// SI LLEGA A 10 HAY OVERFLOW, SE RECARGA Y DA PASO AL INCREMENTO DE LAS DECENAS DE DÍA 
	BRNE FIN_INCREMENTO_DIA
	CLR R16 
	STS C_UNIDAD_DIA, R16  
	LDS R16, C_DECENA_DIA 
	INC R16 
	STS C_DECENA_DIA, R16					 
	CPI R16, 0x4							// LAS DECENAS DE DÍA INCREMENTAN HASTA DETECTA QUE LLEGAN A 4, DE SER ASÍ, ESTA VARIABLE SE REINICIA 
	BRNE FIN_INCREMENTO_DIA
	CLR R16 
	STS C_DECENA_DIA, R16 
	LDS R16, C_UNIDAD_MES
	INC R16 
	STS C_UNIDAD_MES, R16					// INCREMENTA EL CONTADOR DE UNIDADES DE MES, DE SER IGUAL A 10, SE REINICIA 
	CPI R16, 0xA 
	BRNE FIN_INCREMENTO_DIA 
	CLR R16 
	STS C_UNIDAD_MES, R16 
	LDS R16, C_DECENA_MES 
	INC R16									// CUANDO HAY OVERFLOW DE LAS UNIDADES DE MES, INCREMENTA EL CONTADOR DE DECENAS DE MES, EL CUAL NO PUEDE SER MAYOR A 1 
	STS C_DECENA_MES, R16 
	CPI R16, 0x02
	BRNE FIN_INCREMENTO_DIA  
	CLR R16 
	STS C_DECENA_MES, R16

FIN_INCREMENTO_DIA:							// SI NO SE CUMPLEN CONDICIONES DE DESBORDAMIENTO, SE LLEGÁ A ESTA SUBRUTINA
	LDS R16, C_UNIDAD_DIA					// ESTA SUBRUTINA CONTROLA LOS DÍAS A LOS QUE CADA MES PUEDE LLEGAR 
	LDS R17, C_DECENA_DIA 
	LDS R18, C_UNIDAD_MES
	LDS R19, C_DECENA_MES  
ENERO:								
	CPI R19, 0x0 
	BRNE FEBRERO 
	CPI R18, 0x1 
	BRNE FEBRERO 
	CPI R17, 0x3 
	BRNE FEBRERO 
	CPI R16, 0x2 
	BRNE FEBRERO							// SI SE CUMPLEN TODAS LAS CONDICIONES PARA EL DESBORDAMIENTO DE ENERO, SE ESTABLECE FEBERO
	LDI R16, 0x1
	LDI R17, 0x0 
	LDI R18, 0x2
	LDI R19, 0x0 
	STS C_UNIDAD_DIA, R16  
	STS C_DECENA_DIA, R17 
	STS C_UNIDAD_MES, R18 
	STS C_DECENA_MES, R19
	RJMP ACTUALIZACION_DISPLAYS_FECHA		// SE ACTUALIZAN DISPLAYS PARA MOSTRAR LA FECHA ACTUAL 

FEBRERO:									// SIGUE LA MISMA LÓGICA QUE ENERO, TOMANDO EN CUENTA LOS DÍAS A LOS QUE FEBEREO PUEDE LLEGAR
	CPI R19, 0x0	
	BRNE MARZO 
	CPI R18, 0x2 
	BRNE MARZO 
	CPI R17, 0x2 
	BRNE MARZO 
	CPI R16, 0x9 
	BRNE MARZO 
	LDI R16, 0x1
	LDI R17, 0x0 
	LDI R18, 0x3
	LDI R19, 0x0 
	STS C_UNIDAD_DIA, R16  
	STS C_DECENA_DIA, R17 
	STS C_UNIDAD_MES, R18 
	STS C_DECENA_MES, R19
	RJMP ACTUALIZACION_DISPLAYS_FECHA

MARZO: 
	CPI R19, 0x0 
	BRNE ABRIL 
	CPI R18, 0x3 
	BRNE ABRIL 
	CPI R17, 0x3 
	BRNE ABRIL 
	CPI R16, 0x2 
	BRNE ABRIL 
	LDI R16, 0x1
	LDI R17, 0x0 
	LDI R18, 0x4
	LDI R19, 0x0 
	STS C_UNIDAD_DIA, R16  
	STS C_DECENA_DIA, R17 
