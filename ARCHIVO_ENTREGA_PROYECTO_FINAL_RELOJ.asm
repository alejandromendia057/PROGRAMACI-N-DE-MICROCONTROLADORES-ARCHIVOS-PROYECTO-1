PINC_ISR:									// INTERRUPCIÓN POR CAMBIO EN PINC (BOTONES) DETECTA FLANCOS DE BAJADA EN LOS PINES PC0PC4 Y ACTIVA LAS RESPECTIVAS BANDERAS
	PUSH R16
    IN   R16,SREG
    PUSH R16
	PUSH R17 
	PUSH R18 

	IN R16, PINC							
	ANDI R16, 0b0001_1111					// SOLO SE QUEDAN LOS PINES DE LOS BOTONES 
	LDS R17, PINC_ANTERIOR					// SE GUARDA COMO ESTABAN LOS BOTONES ANTES
	STS PINC_ANTERIOR, R16					// SE GUARDA EL ESTADO ACTUAL PARA LA PRÓXIMA OCASIÓN 
	LDI R18, 0b0001_1111					// MÁSCARA PARA BOTONES 
	EOR R18, R16							// XOR ENTRE LA MÁSCARA Y EL ESTADO ANTERIOR DE LOS BOTONES PARA ENCENDER LOS QUE ESTABAN EN 0 (PRESIONADOS POR PULLUP)
	AND R18, R17							// DEJA EN 1 LOS BOTONES QUE ESTABAN PRESIONADOS ANTES 

	MOV R16, R18 
	ANDI R16, 0b0000_0001					// MÁSCARA PARA SOLO LEER UN BOTÓN A LA VEZ
	CPI R16, 0b0000_0001					// COMPARACIÓN PARA DEJAR PASAR 
	BRNE CONTINUA_BOTON2					// DE SER DIFERENTE CONTINUA CON EL SIGUIENTE BOTÓN 
	LDI R16, 0x01							// DE SER IGUAL LA COMPARACIÓN, SE LE CARGA 1 A MODO DE BANDERA A LA VARIABLE INDICADOR_BOTÓN1 
	STS INDICADOR_BOTON1, R16

CONTINUA_BOTON2:
	MOV R16, R18 
	ANDI R16, 0b0000_0010					// MÁSCARA PARA SOLO LEER UN BOTÓN A LA VEZ
	CPI R16, 0b0000_0010					// COMPARACIÓN PARA DEJAR PASAR
