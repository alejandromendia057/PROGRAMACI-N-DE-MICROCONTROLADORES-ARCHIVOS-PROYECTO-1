ISR_TIMER0:									// TIMER0 CADA 250 MS PARA CONTROLAR PARPADEO DE LED EN PC5 Y DURACIÓN DE LA ALARMA
	PUSH R16
    IN   R16,SREG
    PUSH R16

	LDS R16, CONTADOR_ALARMA				// MANEJO DE LA ALARMA: DECREMENTA CONTADOR Y APAGA BUZZER SI LLEGA A 0 			
	CPI R16, 0x0
	BREQ CONTINUA_ISR_0
	DEC R16
	STS CONTADOR_ALARMA, R16 
	CPI R16, 0x0
	BRNE CONTINUA_ISR_0 
	CBI PORTD, PORTD7						// APAGA BUZZER 
	CLR R16
	STS BUZZER_ENCENDIDO, R16 
CONTINUA_ISR_0:

	SBI TIFR0, TOV0							// PONE EN 1 LA BANDERA TOV0 PARA LIMPIAR EL OVERFLOW
	LDI R16, 0x0C
	OUT TCNT0, R16							// VUELVE A COLOCAR VALOR INICIAL AL TIMER (12)

	LDS R16, OVERFLOW_TMR0
	INC R16									// INCREMENTA EL CONTADOR DE DESBORDAMIENTOS OVERFLOW_TMR0
	STS OVERFLOW_TMR0, R16  
	CPI R16, 0x02 
	BRNE FINALIZAR_ISR0 
	CLR R16 
	STS OVERFLOW_TMR0, R16					// CADA 2 DESBORDAMIENTOS CAMBIA ESTADO DEL LED EN PC5 
	IN R16, PORTC							// SE LEE EL VALOR ACTUAL DEL PC5 PARA SABER SI EL LED ESTABA ENCENDIDO O APAGADO 
